const modal = document.getElementById("projectModal");
const modalBody = document.getElementById("modalBody");
const closeButton = document.querySelector(".modal-close");

function openProjectModal(projectId) {
    modalBody.innerHTML = projectDetails[projectId];
    modal.classList.add("show");
    modal.setAttribute("aria-hidden", "false");
    document.body.classList.add("modal-open");
    setupGalleryPreview();
}

function closeModal() {
    const existingPreview = document.querySelector(".image-preview");

    if (existingPreview) {
        existingPreview.remove();
    }

    modal.classList.remove("show");
    modal.setAttribute("aria-hidden", "true");
    document.body.classList.remove("modal-open");
}

document.querySelectorAll(".project-card").forEach((card) => {
    card.addEventListener("click", () => {
        openProjectModal(card.dataset.project);
    });

    card.addEventListener("keydown", (event) => {
        if (event.key === "Enter" || event.key === " ") {
            event.preventDefault();
            card.click();
        }
    });
});

closeButton.addEventListener("click", closeModal);

modal.addEventListener("click", (event) => {
    if (event.target === modal) {
        closeModal();
    }
});

document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
        const existingPreview = document.querySelector(".image-preview");

        if (existingPreview) {
            existingPreview.remove();
            return;
        }

        closeModal();
    }
});

function setupGalleryPreview() {
    document.querySelectorAll(".gallery-item").forEach((item) => {
        item.addEventListener("click", () => {
            const image = item.querySelector("img");
            const caption = item.querySelector("span").textContent;
            const existingPreview = document.querySelector(".image-preview");

            if (existingPreview) {
                existingPreview.remove();
            }

            modalBody.insertAdjacentHTML("beforeend", `
                <div class="image-preview">
                    <button class="image-preview-close" type="button" aria-label="Close image preview">&times;</button>
                    <img src="${image.getAttribute("src")}" alt="${image.alt}">
                    <p>${caption}</p>
                </div>
            `);

            document.querySelector(".image-preview-close").addEventListener("click", () => {
                document.querySelector(".image-preview").remove();
            });

            document.querySelector(".image-preview").addEventListener("click", (event) => {
                if (event.target.classList.contains("image-preview")) {
                    event.currentTarget.remove();
                }
            });
        });
    });
}
